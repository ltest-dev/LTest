(** Initializer of .labels file *)

open Commons

let compute ?(force=false) data =
  if force then Data.clear data;
  Instrument.iter (fun id info ->
    let tag = info.Instrument.li_tag in
    let current_loc = location_string info.Instrument.li_loc in
    Data.update data ~force ~tag ~current_loc ~status:Data.Unknown id
  )
